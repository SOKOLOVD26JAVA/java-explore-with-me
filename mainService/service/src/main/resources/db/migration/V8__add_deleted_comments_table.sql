CREATE TABLE IF NOT EXISTS deleted_comments (
 id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
 reason VARCHAR(500) NOT NULL,
 old_comment_id BIGINT NOT NULL,
 author_id BIGINT NOT NULL,
 event_id BIGINT NOT NULL,
 admin_id BIGINT NOT NULL,
 created TIMESTAMP NOT NULL,
 deleted_at TIMESTAMP NOT NULL,
 text VARCHAR(2000),
 CONSTRAINT FK_DELETED_COMMENT_AUTHOR FOREIGN KEY (author_id) REFERENCES users(id) ON DELETE CASCADE,
 CONSTRAINT FK_DELETED_COMMENT_EVENT FOREIGN KEY (event_id) REFERENCES events(id) ON DELETE CASCADE,
 CONSTRAINT FK_DELETED_COMMENT_ADMIN FOREIGN KEY (admin_id) REFERENCES users(id) ON DELETE CASCADE
 );
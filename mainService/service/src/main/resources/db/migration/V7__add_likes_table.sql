CREATE TABLE IF NOT EXISTS likes (
 id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
 rating BIGINT NOT NULL,
 comment_id BIGINT NOT NULL,
 liker_id BIGINT NOT NULL,
 CONSTRAINT FK_AUTHOR_COMMENT FOREIGN KEY (comment_id) REFERENCES comments(id) ON DELETE CASCADE,
 CONSTRAINT FK_LIKER_USER FOREIGN KEY (liker_id) REFERENCES users(id) ON DELETE CASCADE,
 CONSTRAINT UNIQUE_LIKER UNIQUE (comment_id, liker_id),
 CONSTRAINT CHK_RATING CHECK (rating BETWEEN 1 AND 5)
);
CREATE TABLE IF NOT EXISTS comments (
 id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
 text VARCHAR(2000) NOT NULL,
 event_id BIGINT NOT NULL,
 author_id BIGINT NOT NULL,
 created TIMESTAMP NOT NULL,
 deleted_by BIGINT,
  deleted_at TIMESTAMP,
  reason VARCHAR(500),
 CONSTRAINT FK_AUTHOR_EVENTS FOREIGN KEY (event_id) REFERENCES events(id) ON DELETE CASCADE,
 CONSTRAINT FK_AUTHOR_USER FOREIGN KEY (author_id) REFERENCES users(id) ON DELETE CASCADE,
 CONSTRAINT FK_DELETED_COMMENT_ADMIN FOREIGN KEY (deleted_by) REFERENCES users(id) ON DELETE CASCADE
);
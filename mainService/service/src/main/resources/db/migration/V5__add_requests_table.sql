CREATE TABLE IF NOT EXISTS requests (
 id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
 created TIMESTAMP NOT NULL,
 event_id BIGINT NOT NULL,
 requester_id BIGINT NOT NULL,
 status VARCHAR(50) NOT NULL,
 CONSTRAINT FK_REQUESTS_EVENTS FOREIGN KEY (event_id) REFERENCES events(id) ON DELETE CASCADE,
 CONSTRAINT FK_REQUEST_REQUESTER FOREIGN KEY (requester_id) REFERENCES users(id) ON DELETE CASCADE,
 CONSTRAINT UQ_EVENT_REQUESTER UNIQUE (event_id, requester_id)
);